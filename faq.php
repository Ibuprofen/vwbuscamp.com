<div class="header">Q. Is Bus Camp  loud? </div>
<div class="contentSection">
    <div class="content">A. Define loud? Burning Man can be an incredibly noisy event, and not  everyone wants to sleep when you do (many people don't sleep at all during the  event). Even earplugs can't mask the din of the noise at times. That being said,  we are a generator free camp, and we are not located on the esplanade or  anywhere the near the &quot;rave&quot; camps, which makes our camp is more quiet that at  other places on the playa. </div>
</div>

<div class="header">Q. I'm going this year  what should I do? </div>
<div class="contentSection">
    <div class="content">A. First read the <a href="http://www.burningman.com/preparation/event_survival/" target="_blank">survival guide</a>,  then read it again, then print it out and read it whenever you going to the  bathroom. Then if you think you still really want to go, buy a ticket. I  recommend you purchase your ticket in advance or use the will call at the gate  (I have done this in the past with no problems). Beware of counterfeit tickets,  if you buy a fake, you will have to purchase a real ticket again at the gate- my  advice is to only buy your ticket from the Bmorg or someone you know personally.  Go to the Bus Camp <a href="http://vwbuscamp.com/mailman/listinfo/vwbuscamp_vwbuscamp.com">discussion list</a> or <a href="http://bbs.burningman.com/" target="_blank">eplaya</a>, make some friends and start  packing </div>
</div>

<div class="header">Q. Do I really need to  take a bike? </div>
<div class="contentSection">
    <div class="content">A. Yes, I think a bike is essential. The city has just gotten  to big to navigate on foot. Leave the $1200 mtn bike with 26 gears and gas  filled shocks at home. Buy a beater bike, a one speed beach cruiser with coaster  brake is optimal for the playa. For safe night ridding , a flashing red LED in  back and a white light in front work great. I have seen some nasty collisions at  night between cyclists that didn't see each other. The lights also help you keep  track of your group when cruising around, especially if they are unique. For  more information see the Burning Man <a href="http://www.burningman.com/preparation/event_survival/bikes.html" target="_blank">Bike  Guide</a> </div>

