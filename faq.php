<div class="header">Q. What is a theme  camp? </div>
<div class="contentSection">
    <div class="content">A. A theme camp is a group of people that reserve a section of space  at Burning Man and camp together. Some theme camps operate bars, others conspire  to build art, Bus Camp seeks to unite and support those people irrational enough  to drive their Volkswagens to the desert. More on <a href="http://www.burningman.com/themecamps_installations/" target="_blank">Theme  Camps</a>.</div>
</div>

<div class="header">Q. So what do you guys  do? Sit around and talk about Busses? </div>
<div class="contentSection">
    <div class="content">A. The funny thing about Bus Camp is  that, generally speaking, the only thing we have in common is our vehicle of  choice. This creates a wonderfully diverse group of people with different  personalities and skills. We have had E.R. doctors, computer programmers,  writers, aircraft designers, psychologists, film makers, architects and even an  NASA rocket scientist camp with us. Bus Camp is really a community of people  with different interests but one goal- To Participate. Our camp is always  involved in a multitude of projects at Burning Man from serving as Black Rock  Rangers, to building rave camps. We also have a cocktail hour and potluck dinner  every day at sunset. </div>
</div>

<div class="header">Q. What do I have to do  to join Bus Camp? </div>
<div class="contentSection">
