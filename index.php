<div class="header">Once Upon a Time - The History of VW Bus Camp</div>
<div class="contentSection">
    <div class="content">
        Well, it all started innocently enough in 1998. I had heard about Burning Man, on a flyer actually, someone had made this crude drawing of the &quot;the man&quot; and I was just drawn to it. I checked out the BM website and decided that this theme camp thing was the way to go. I searched the list of theme camps looking to find that place where I would fit. I found lots of cool camps, but nothing seemed quite right. Almost by accident, I mentioned my plans to go to Burning Man to my friend Ron &quot;Coyote&quot;, yes, that &quot;Coyote&quot;, and since we were both bus owners, he said &quot;why not a VW Bus Camp?&quot; - Of course, why hadn't I thought of it before. Well, a few weeks later Sherry Lewis, yes that &quot;Sherry Lewis&quot;, of Sherry Lewis and Lamb Chop, passed away. Ron C. was very upset and wanted to honor her memory with the &quot;Sherry Lewis and Lambchop Martini Bar and Shrine&quot;. So I was left holding the bag wondering if I should even go through with it, but Ron C. pushed me on. So I did. The big plan was to arrange the busses in a spiral, like a giant crop circle, OK, so that didn't really work out, people parked any way the pleased and we awoke each morning to one or two new arrivals wedged in this way and that way, but we had an amazing time that year, and every year since. It just keeps getting better and better. I'm always amazed at how many people (including myself) keep coming back, we must be doing something right. If you have ever camped with us, then you're part of our family. And everybody that pulls into our little camp gets a great big hug and a &quot;Welcome Home&quot;! - Ron
